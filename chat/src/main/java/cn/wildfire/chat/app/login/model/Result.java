package cn.wildfire.chat.app.login.model;

public class Result<T> {

    /** code */
    private int code;

    /** msg */
    private String msg;

    /** data */
    private T data;

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public T getData() {
        return data;
    }

    public void setData(T data) {
        this.data = data;
    }
}
